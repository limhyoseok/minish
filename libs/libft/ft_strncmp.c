#include "libft.h"

int		ft_strncmp(const char *s1, const char *s2, size_t len)
{
	while (((*s1 != 0) || (*s2 != 0)) && (len-- > 0))
	{
		if (*s1 != *s2)
			return (unsigned char)*s1 - *s2;
		s1++;
		s2++;
	}
	return (0);
}
